    private void init() {
        initDialogText();
        initValidation();
    }

    private void initDialogText() {
        tfFirstName.setText(personToEdit.getFirstName());
        tfLastName.setText(personToEdit.getLastName());
    }

    private boolean formValid() {
        boolean ok = true;
        for (int i = 0; i < validationFields.size(); i++) {
            ok &= !validationFields.get(i).getText().trim().isEmpty();
            errorLabels.get(i).setText(validationFields.get(i).getText().trim().isEmpty() ? ERROR_MARK : "");
        }
        return ok;
    }
    

    private void initValidation() {
        validationFields = Arrays.asList(tfFirstName, tfLastName);
        errorLabels = Arrays.asList(lbFirstNameError, lbLastNameError);
    }

    private boolean detailsChanged() {
        return !personToEdit.getFirstName().equals(tfFirstName.getText()) || !personToEdit.getLastName().equals(tfLastName.getText());
    }

