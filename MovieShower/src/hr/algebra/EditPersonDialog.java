    private void btnChangeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnChangeActionPerformed
        if (formValid() && detailsChanged()) {
            Person newDetails = new Person(tfFirstName.getText(), tfLastName.getText());
            try {
                if (!personEditable.editPerson(personToEdit, typeOfPerson, newDetails)) {
                    MessageUtils.showInformationMessage(FAILED_EDIT, FAILED_EDIT_MESSAGE);
                    return;
                }
            } catch (Exception ex) {
                Logger.getLogger(EditPersonDialog.class.getName()).log(Level.SEVERE, null, ex);
            }
            dispose();
        } else {
            MessageUtils.showInformationMessage(WRONG_OPERATION, WRONG_OPERATION_MESSAGE);
        }

    }//GEN-LAST:event_btnChangeActionPerformed
    
    

